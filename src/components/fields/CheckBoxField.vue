<template>
    <div class="relative flex items-start mt-2">
        <div class="flex items-center h-5 mt-0.5">
            <input :id="name" :aria-describedby="label + '-description'" :name="label" type="checkbox" class="focus:ring-indigo-500 h-4 w-4 text-indigo-600 border-gray-300 rounded"
                   v-on:input="$emit('update:modelValue', $event.target.value)" v-bind:value="modelValue" :ref="name"
            />
        </div>
        <div class="ml-2 text-sm">
            <label :for="name" class="font-medium text-gray-700">{{ label }}</label>
            <p v-if="description" class="text-sm text-gray-500 max-w-md">{{ description }}</p>
            <p v-if="error" class="mt-2 text-sm text-red-600">{{ error }}</p>
        </div>

    </div>
</template>

<script>
export default {
    name: "CheckBoxField",
    props: {
      // eslint-disable-next-line vue/require-prop-type-constructor
        modelValue: String|Boolean,
        form: Object,
        name: String,
        label: String,
        placeholder: String,
        description: String,
        required: Boolean,
        disabled: Boolean,
    },
    mounted() {
        if (typeof this.modelValue === "string") {
            this.$emit('update:modelValue', this.modelValue === "true");
        }
    },
    computed: {
        error() {
            return null
        }
    }
}
</script>
